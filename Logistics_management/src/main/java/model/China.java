package model;

public class China {
    private String China_Id;
    private String China_Name;
    private String China_Pid;

    public China() {

    }

    public String getChina_Id() {
        return China_Id;
    }

    public void setChina_Id(String china_Id) {
        China_Id = china_Id;
    }

    public String getChina_Name() {
        return China_Name;
    }

    public void setChina_Name(String china_Name) {
        China_Name = china_Name;
    }

    public String getChina_Pid() {
        return China_Pid;
    }

    public void setChina_Pid(String china_Pid) {
        China_Pid = china_Pid;
    }
}